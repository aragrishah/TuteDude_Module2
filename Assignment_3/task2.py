# Using the Math Module for Calculations

import math

n = int(input("Enter a number: "))
square_root = math.sqrt(n)
natural_log = math.log(n)
sine_value = math.sin(n) 

print(f"Square root of {n} is {square_root}")
print(f"Natural logarithm of {n} is {natural_log}")
print(f"Sine of {n} (in radians) is {sine_value}")
